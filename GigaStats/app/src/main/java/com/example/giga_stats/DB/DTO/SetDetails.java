package com.example.giga_stats.DB.DTO;

public class SetDetails {
    public int weight;
    public int reps;

    public SetDetails() {
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getReps() {
        return reps;
    }

    public void setReps(int reps) {
        this.reps = reps;
    }
}
